org 9000H		;MAIN EXECUTION SEQUENCE
	MVI A, 8BH
	OUT 43H
	
	MAINLOOP:
	IN 41H
	CMA
	ANI 40H
	JZ HERE
	JMP PROGRAM
	HERE:
	JMP MAINLOOP

	PROGRAM:
	MVI A, 01H
	PROGRAMLOOP:
	OUT 40H
	RAR
	MOV C, A
	IN 41H
	CMA
	ANI 20H
	JZ HERE1
	JMP ENDER
	HERE1:
	MOV A, C
	CALL DELAY1		
	JMP PROGRAMLOOP
	ENDER:
RST 5

DELAY1:
MVI C, 0AH
LOOP:         
MVI D, 64H
LOOP1:       
MVI E, 0DEH
LOOP2:       
DCR E
JNZ LOOP2
DCR D
JNZ LOOP1
DCR C
JNZ LOOP
RET
