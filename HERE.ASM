cpu "8085.tbl"
hof "int8"

org 9000H		;MAIN EXECUTION SEQUENCE
	MVI A, 8BH
	OUT 43H
	MVI A, 01H
	
	MAINLOOP:
	IN 50H
	CMA
	ANI 40H
	CPI 00H
	JZ HERE
	JMP PROGRAMLOOP
	HERE:
	JMP MAINLOOP

	PROGRAMLOOP:
	MOV C, A
	OUT 40H
	RAR
	IN 50H
	CMA
	ANI 20H
	CPI 00H
	JZ HERE1
	JMP ENDER
	HERE1:
	MOV A, C
	CALL DELAY1		
	JMP PROGRAMLOOP
	ENDER:
RST 5

DELAY1:
MVI C, 0AH
LOOP:         
MVI D, 64H
LOOP1:       
MVI E, 0DEH
LOOP2:       
DCR E
JNZ LOOP2
DCR D
JNZ LOOP1
DCR C
JNZ LOOP
RET
